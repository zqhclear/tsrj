package org.tsrj.common.amp.enums;

/**
 * @Author: qh-zhong
 * @Date: 2018/5/9
 */
public enum MethodType {
    GET("get", "get请求"),
    POST("post", "post请求"),
    DELETE("delete", "delete请求"),
    PATCH("patch", "patch请求"),
    ;

    MethodType(String type, String desc) {
        this.type = type;
        this.desc = desc;
    }

    private String type;
    private String desc;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
